function decode_response($string, $start, $end){

  $string = ' ' . $string;

  $ini = strpos($string, $start);

  if ($ini == 0) return '';

  $ini += strlen($start);

  $len = strpos($string, $end, $ini) - $ini;

  return substr($string, $ini, $len);

}





  function  getcolumnname($table,$columm,$id)

    {

        $query =  DB::table($table);

        $query->where('id',$id);

        $rows = $query->first();

        if(!empty($rows)){

          return $rows->$columm;

        }else{

            return ''; 

        }

    }

    

    

